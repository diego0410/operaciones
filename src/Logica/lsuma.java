
package Logica;



public class lsuma {
    int resultado;
// metodo sumar
    public int Sumar(int valor1, int valor2){
        resultado= valor1+valor2;
        return resultado;
    }
 // metodo restar
    public int Restar(int valor1, int valor2){
        resultado= valor1-valor2;
        return resultado;
    }
}
